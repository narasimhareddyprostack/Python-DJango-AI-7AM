#div  - /       - always float value
#floor div - // - might be int value or float


#div  - /       - always float value
print(10/5)   #2.0

#floor div - // - might be int value or float
print(10//5)  #2
print(10//5.0)  #2.0

print(3/2)   #1.5
print(3//2)   #1     - floor value
print(3//2.0)   #1.0 - floor 